#include "push_swap.h"

void		ra(int **a, int size_a)
{
	int i;

	if (size_a <= 1)
		return ;
	i = 0;
	while (i < size_a - 1)
	{
		ft_swap((*a) + i, (*a) + i + 1);
		i++;
	}
}
