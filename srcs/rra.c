#include "push_swap.h"

void		rra(int **a, int size_a)
{
	int i;

	if (size_a <= 1)
		return ;
	i = size_a - 1;
	while (i > 0)
	{
		ft_swap(&(*a)[i], &(*a)[i - 1]);
		i--;
	}
}
